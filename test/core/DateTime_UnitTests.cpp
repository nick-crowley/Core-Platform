TEST(DateTime_UT, defaultConstructor_Succeeds) 
{
	//! @test  Verify @c core::DateTime default c-tor succeeds
	EXPECT_NO_THROW(
		DateTime{}
	);
}

TEST(DateTime_UT, now_Succeeds) 
{
	//! @test  Verify @c core::DateTime::now() succeeds
	EXPECT_NO_THROW(
		DateTime::now()
	);
}

TEST(DateTime_UT, str_Succeeds) 
{
	//! @test  Verify @c core::DateTime::str() succeeds and returns expected value
	EXPECT_EQ(
		"Thu Jan  1 00:00:00 1970",
		DateTime{}.str()
	);
}

TEST(DateTime_UT, equalityOperator_ReturnsTrue) 
{
	DateTime a = DateTime::now(), b = a;

	//! @test  Verify @c core::DateTime compare equal when the same
	EXPECT_EQ(
		a, b
	);
}

TEST(DateTime_UT, inequalityOperator_ReturnsTrue) 
{
	DateTime a = DateTime::now(), b;

	//! @test  Verify @c core::DateTime compare unequal when different
	EXPECT_NE(
		a, b
	);
}

