namespace nstd {

	/* ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` */ /*!
	* @brief	Defines the member types required for iterator traits
	*/
	template <typename Value,
			  typename Category,
			  typename Reference  = std::add_lvalue_reference_t<Value>,
			  typename Pointer    = std::add_pointer_t<Value>,
			  typename Difference = std::ptrdiff_t>
	struct make_iterator_traits_t
	{
		using difference_type   = Difference;      //!< Distance type (between two iterators)
		using value_type        = Value;           //!< Value type
		using pointer           = Pointer;         //!< Pointer-to-value type
		using reference         = Reference;       //!< Reference type
		using iterator_category = Category;        //!< Iterator category
	};

}      // namespace nstd
