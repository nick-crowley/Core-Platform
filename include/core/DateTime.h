namespace core
{	
	/* ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` */ /*!
	* @brief	UNIX-style date/time with 1-second accuracy
	*/
	class DateTime
	{
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Types & Constants o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	private:
		//! @brief 	Aliases our own type
		using type = DateTime;
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=o Representation o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	private:
		std::time_t	 Epoch = 0;
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Construction & Destruction o=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		/* ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` */ /*!
		* @brief  Construct from UNIX epoch time
		*
		* @param[in]  epoch	 Seconds since 1st Jan 1970
		*/
		explicit
		DateTime(std::time_t epoch) : Epoch(epoch)
		{}
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~o Copy & Move Semantics o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		satisfies(DateTime, 
			constexpr IsRegular noexcept
		);
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=o Static Methods o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		/* ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` */ /*!
		* @brief  Retrieve current time
		*
		* @returns  Current time
		*/
		DateTime
		static now() {
			return DateTime{std::time(nullptr)};
		}
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~o Observer Methods & Operators o~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		/* ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` */ /*!
		* @brief  Generate string representation 
		*
		* @returns  Time formatted as 'Www Mmm dd hh:mm:ss yyyy'
		*/
		std::string
		str() const {
			std::string result = std::ctime(&this->Epoch);
			while (result.ends_with('\n'))
				result.pop_back();
			return result;
		}

		/* ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` */ /*!
		* @brief  Generate string representation 
		*
		* @param[in]  format  C-style time formatting string
		*
		* @returns	Formatted time string
		*
		* @throws	std::invalid_argument  Invalid formatting string
		* 
		* @remarks	'c' standard date/time string
		* @remarks	'Ec' alternative date/time string
		*
		* @remarks	'x' date string (localized)
		* @remarks	'X' date string (localized)
		*
		* @remarks	'Y' four digit year
		* @remarks	'y' two digit year
		* @remarks	'C' two digit year
		* @remarks	'B' full month name
		* @remarks	'b' abreiviated month name
		* @remarks	'A' full day name
		* @remarks	'H' hours (24-hour notation)
		* @remarks	'I' hours (12-hour notation)
		* @remarks	'M' minutes
		* @remarks	'S' seconds
		*
		* @remarks	'F' equivalent to "%Y-%m-%d" (the ISO 8601 date format) 
		* @remarks	'D' equivalent to "%m/%d/%y"
		*/
		std::string
		str(std::string_view format) const {
			// Convert epoch time into calendar format
			std::tm properties {};
			::gmtime_s(&properties, &this->Epoch);

			// Assemble string representation by formatting calendar time
			std::string buffer(size_t{256}, '\0');
			if (auto const length = std::strftime(&buffer[0], buffer.length(), format.data(), std::gmtime(&this->Epoch)); !length) 
				ThrowInvalidArg(format, "Unable to parse '{}'", format);
			else {
				buffer.resize(length);
				return buffer;
			}
		}
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Mutator Methods & Operators o~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	};
}
// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Non-member Methods o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~o
namespace core
{
	//! @brief	Convert @c core::DateTime to string
	std::string
	inline to_string(const DateTime& dt) {
		return dt.str();
	}
}
