//! @brief	Throws if enumerator @c e is not a valid enumerator
//! 
//! @param	e		Enumerator
#define ThrowIfUndefined(e)  ::core::detail::ThrowIfUndefinedImpl(e, nameof(e))

// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o Constants & Enumerations o~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~o

// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Class Declarations o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~o
namespace core::detail
{
	template <typename E> 
		requires nstd::Enumeration<std::remove_cvref_t<E>>
	decltype(auto) 
	ThrowIfUndefinedImpl(E&& value, char const* argName, std::source_location loc = std::source_location::current())
	{
		if (!is_valid_enumerator(value)) {
			throw invalid_argument{"{}(..) Invalid enumerator argument '{}' ({})", loc.function_name(), argName, to_hexString(value)};
		}

		return std::forward<E>(value);
	}
}
