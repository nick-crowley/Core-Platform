namespace core::win
{
	class ConstMultiStringIterator 
		: public boost::iterator_facade<ConstMultiStringIterator, std::wstring_view const, std::forward_iterator_tag>
	{
		friend class boost::iterator_core_access;
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Types & Constants o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	private:
		using type = ConstMultiStringIterator;

		std::wstring_view const
		inline static npos;
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=o Representation o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	private:
		std::wstring_view  Current;
		std::wstring_view  Source;
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Construction & Destruction o=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		ConstMultiStringIterator()
		  : Source{npos}
		{
		}

		ConstMultiStringIterator(std::wstring_view src)
		  : Source{src}
		{
		}
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~o Copy & Move Semantics o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		satisfies(ConstMultiStringIterator,
			IsCopyable,
			IsMovable
		);
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=o Static Methods o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o

		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~o Observer Methods & Operators o~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	private:
		bool 
		equal(type const& other) const
		{
			return this->Current == other.Current;
		}

		std::wstring_view const& 
		dereference() const 
		{ 
			return this->Current; 
		}
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Mutator Methods & Operators o~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	private:
		void
		increment() 
		{ 
			if (this->Current.front() == L'\0')
				this->Current = npos;
			else
				this->Current.remove_prefix(this->Current.find(L'\0')+1);
		}
	};
}
