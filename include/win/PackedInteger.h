namespace core::win 
{
	//! @brief	Provides conversion between 64-bit integers stored as two 32-bit numbers
	template <nstd::AnyOf<signed,unsigned> T>
	union PackedInteger;

	//! @brief  Specialization for signed 64-bit integers
	template <>
	union PackedInteger<signed> {
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Types & Constants o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
		struct SignedUnsigned {
			std::uint32_t  Low;
			std::int32_t   High;
		};
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=o Representation o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		SignedUnsigned  Split;
		std::int64_t    Whole;
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Construction & Destruction o=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		constexpr
		PackedInteger(std::uint32_t low, std::int32_t high) : Split{low, high}
		{}

		constexpr explicit
		PackedInteger(std::int64_t big) : Whole(big)
		{}
	};

	//! @brief  Specialization for unsigned 64-bit integers
	template <>
	union PackedInteger<unsigned> {
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Types & Constants o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
		struct TwoUnsigned {
			std::uint32_t  Low;
			std::uint32_t  High;
		};
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=o Representation o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		TwoUnsigned    Split;
		std::uint64_t  Whole;
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Construction & Destruction o=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		constexpr
		PackedInteger(std::uint32_t low, std::uint32_t high) : Split{low, high}
		{}

		constexpr explicit
		PackedInteger(std::uint64_t big) : Whole(big)
		{}
	};
}
