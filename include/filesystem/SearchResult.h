namespace core::filesystem {

	//! @brief	Single result from a search of the file system
	struct SearchResult
	{
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Types & Constants o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o

		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=o Representation o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		nstd::bitset<FileAttribute> Attributes;    //!< File/directory attributes
		path                        AbsolutePath;  //!< Absolute path of file/directory
		filesize_t                  Size = 0;      //!< [file-only] File size, in bytes
		FileTime                    LastModified;  //!< Last access/modification times
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Construction & Destruction o=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		//! @brief  Construct from components
		SearchResult(FileAttribute attr, path absPath, filesize_t size, FileTime modified)
		  : Attributes{attr}, 
		    AbsolutePath{absPath},
		    Size{size}, 
			LastModified{modified}
		{}
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~o Copy & Move Semantics o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		satisfies(SearchResult,
			IsSemiRegular,
			NotEqualityComparable,
			NotSortable
		);
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=o Static Methods o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o

		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~o Observer Methods & Operators o~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		//! @brief  Query whether result is a directory
		bool
		directory() const {
			return this->category() == EntryCategory::Directory;
		}

		//! @brief	Query file-system entry category
		EntryCategory
		category() const {
			return this->Attributes.test(FileAttribute::Directory) ? EntryCategory::Directory 
			                                                       : EntryCategory::File;
		}
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Mutator Methods & Operators o~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	};

}
