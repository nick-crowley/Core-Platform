namespace core::filesystem 
{	
	//! @brief	Common file-size type
	using filesize_t = std::uint64_t;
}

namespace core::filesystem::literals
{
	inline namespace filesize_literals 
	{
		//! @brief  File size, in kilobytes
		filesize_t constexpr
		operator"" _kb(unsigned long long n) noexcept {
			return n * 1024;
		}

		//! @brief  File size, in megabytes
		filesize_t constexpr 
		operator"" _mb(unsigned long long n) noexcept {
			return n * 1024 * 1024;
		}

		//! @brief  File size, in gigabytes
		filesize_t constexpr 
		operator"" _gb(unsigned long long n) noexcept {
			return n * 1024 * 1024 * 1024;
		}
	}
} 
