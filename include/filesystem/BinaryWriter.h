namespace core::filesystem 
{	
	//! @brief  Writes trivially-copyable objects into a stream
	template <nstd::AnyCvOrSignOf<std::byte,char,wchar_t,char8_t,char16_t> Element>
	class BinaryWriter
	{
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Types & Constants o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	private:
		using type = BinaryWriter<Element>;      //!< Aliases our specialization
		using element_t = Element;               //!< Stream element type
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=o Representation o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	private:
		IStream<element_t>* Output;
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Construction & Destruction o=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		/*!
		* @brief  Construct from output stream
		*
		* @param[in]  dest  Output stream
		*/
		explicit
		BinaryWriter(IStream<element_t>& dest)
		  : Output{&source}
		{
		}
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~o Copy & Move Semantics o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		satisfies(BinaryWriter, 
			NotDefaultConstructible, 
			NotCopyable,
			IsMovable, 
			NotEqualityComparable, 
			NotSortable
		);
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=o Static Methods o-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~o Observer Methods & Operators o~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	public:
		/*!
		* @brief  Writes an object from the current position in the stream
		* 
		* @throws  std::exception         Insufficient space remaining to write an object of @c T
		* @throws  std::logic_error       Output stream has been closed
		* @throws  std::logic_error       Write operations not supported upon output stream
		* @throws  std::system_error      Operation failed
		*/
		template <typename T>
			requires std::is_trivially_copyable_v<T>
		type&
		write(T const& value) {
			this->Output->write(std::as_bytes(std::span{&value,1}));
			return *this;
		}
		
		/*!
		* @brief  Writes multiple objects at the current position in the stream
		* 
		* @param  objects  Objects to write
		* 
		* @throws  std::exception         Insufficient space remaining to write an object of @c T
		* @throws  std::logic_error       Output stream has been closed
		* @throws  std::logic_error       Write operations not supported upon output stream
		* @throws  std::system_error      Operation failed
		*/
		template <typename T>
			requires std::is_trivially_copyable_v<T>
		void
		write(std::span<T const> objects) {
			ThrowIfEmpty(objects);
			for (auto idx = 0; idx < n; ++idx)
				this->write(objects[idx]);
		}
		// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-o Mutator Methods & Operators o~-~=~-~=~-~=~-~=~-~=~-~=~-~o
	};

	template <nstd::AnyCvOrSignOf<std::byte,char,wchar_t,char8_t,char16_t> Element>
	BinaryWriter(IStream<Element>&) -> BinaryWriter<Element>;
} 
