namespace core::win {

	/* ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` ` */ /*!
	* @brief	Access-rights for services objects
	*/
	enum class ServiceRight : access_mask_t {
		AllAccess = SERVICE_ALL_ACCESS,                     //!< All access rights
		ChangeConfig = SERVICE_CHANGE_CONFIG,               //!< Change the service configuration (also grants the right to change its executable file).
		EnumerateDependents = SERVICE_ENUMERATE_DEPENDENTS, //!< Enumerate all the services dependent on the service.
		Interrogate = SERVICE_INTERROGATE,                  //!< Ask the service to report its status immediately.
		PauseContinue = SERVICE_PAUSE_CONTINUE,             //!< Pause or continue the service.
		QueryConfig = SERVICE_QUERY_CONFIG,                 //!< Query the service configuration.
		QueryStatus = SERVICE_QUERY_STATUS,                 //!< Ask about the status of the service or receive notifications when it changes.
		Start = SERVICE_START,                              //!< Start the service.
		Stop = SERVICE_STOP,                                //!< Stop the service.
		UserDefinedControl = SERVICE_USER_DEFINED_CONTROL,  //!< Call the ControlService function to specify a user-defined control code.
	};
	
	enum class CommonRight : access_mask_t;
	enum class GenericRight : access_mask_t;
	enum class StandardRight : access_mask_t;
}
// o~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~o Constants & Enumerations o~-~=~-~=~-~=~-~=~-~=~-~=~-~=~-~=~o
